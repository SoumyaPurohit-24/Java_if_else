package ex_01_operators;

public class Java_Lab004_Alloperator {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        System.out.println(a+b);
        System.out.println(a==b);
        System.out.println(a>b);
        System.out.println(a<=b);
        System.out.println(a!=b);
    }
}
