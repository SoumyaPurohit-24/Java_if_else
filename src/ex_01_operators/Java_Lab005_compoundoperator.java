package ex_01_operators;

public class Java_Lab005_compoundoperator {
    static void main(String[] args) {
        int age = 10;
        age += 10;
        System.out.println(age);//that means age+10
        age -=10;
        System.out.println(age);
        age /=10;
        System.out.println(age);  //compound operator
    }
}
